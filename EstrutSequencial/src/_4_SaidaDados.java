public class _4_SaidaDados {
    public static void main(String[] args) {
        // O comando básico para saída de dados qualquer é:
        // "SYSTEM.OUT.PRINT". O 'print' não tem quebra de linha no final, exemplo:
        System.out.print("Hello World!!!");
        System.out.println("Bom dia");

        // O 'println' realiza a quebra de linha no final, exemplo:
        System.out.println();
        System.out.println("What a wonderful day to be alive");
        System.out.println("What a wonderful World");

        // Para escrever na tela o conteúdo de uma variável de algum tipo básico:
        int y = 32;
        System.out.println(y);

    }
}
